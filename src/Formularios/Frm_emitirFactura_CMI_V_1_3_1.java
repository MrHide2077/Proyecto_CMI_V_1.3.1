    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int filaSel = tab_detalles.getSelectedRow();

    try {
        String servicio = txt_servicio.getText().trim();
        String cantidad = txt_cantidad.getText().trim();
        String precioStr = txt_precio.getText().trim();

        if (servicio.isEmpty()) {
            txt_servicio.requestFocus();
            JOptionPane.showMessageDialog(null, "Ingrese el servicio");
            return;
        }
        if (cantidad.isEmpty()) {
            txt_cantidad.requestFocus();
            JOptionPane.showMessageDialog(null, "Ingrese la cantidad");
            return;
        }
        int cant = Integer.parseInt(cantidad);
        if (cant <= 0) {
            txt_cantidad.requestFocus();
            JOptionPane.showMessageDialog(null, "Ingrese una cantidad mayor a 0");
            return;
        }
        if (precioStr.isEmpty()) {
            txt_precio.requestFocus();
            JOptionPane.showMessageDialog(null, "Ingrese el precio");
            return;
        }
        double precio = Double.parseDouble(precioStr);

        modeloDetalles = (DefaultTableModel) tab_detalles.getModel();

        if (filaSel != -1) {
            tab_detalles.setValueAt(servicio, filaSel, 0);
            tab_detalles.setValueAt(cantidad, filaSel, 1);
            tab_detalles.setValueAt(String.valueOf(precio), filaSel, 2);
        } else {
            String nuevaFila[] = {servicio, cantidad, String.valueOf(precio)};
            modeloDetalles.addRow(nuevaFila);
        }

        try {
            mtd_calculosTabDet();
        } catch (Exception e) {
            System.err.println("Error omitido en calculos: " + e.getMessage());
        }

        try {
            mtd_limpiarDetalles();
        } catch (Exception e) {
            txt_servicio.setText("");
            txt_cantidad.setText("");
            txt_precio.setText("");
        }

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Porfavor ingrese un número valido");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error general: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed


