    public boolean mtd_duplicadoFact() {
        boolean duplicado = false;
        for (int i = 0; i < tab_facturas.getRowCount(); i++) {
            if (txt_numFact.getText().equals(tab_facturas.getValueAt(i, 0).toString())) {
                duplicado = true;
                break;
            }
        }
        return duplicado;
    }

    private void mtd_calculosTabDet() {
    double subtotal = 0;
    int totalFilas = tab_detalles.getRowCount();

    if (totalFilas <= 0) {
        txt_subtotal.setText("0.0");
        txt_total.setText("0.0");
        return;
    }

    for (int i = 0; i < totalFilas; i++) {
        try {
            int cantidad = Integer.parseInt(tab_detalles.getValueAt(i, 1).toString().trim());
            double precio = Double.parseDouble(tab_detalles.getValueAt(i, 2).toString().trim());
            subtotal += (cantidad * precio);
        } catch (Exception e) {
        }
    }

    txt_subtotal.setText(String.valueOf(subtotal));

    try {
        double descuento = 0;
        String descSel = txt_descuento.getSelectedItem().toString().trim();
        if (!descSel.equals("--Seleccione--") && !descSel.isEmpty()) {
            descuento = Double.parseDouble(descSel);
        }
        
        double totalFinal = subtotal - (subtotal * (descuento / 100));
        txt_total.setText(String.valueOf(totalFinal));
    } catch (Exception e) {
        txt_total.setText(String.valueOf(subtotal));
    }
}



