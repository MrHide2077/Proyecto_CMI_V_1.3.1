    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int filaSel = tab_facturas.getSelectedRow();

        try {
            if (txt_apellidos.getText().trim().isEmpty()) {
                txt_apellidos.requestFocus();
                throw new IllegalArgumentException("Ingrese los Apellidos del Paciente");
            }
            if (txt_nombres.getText().trim().isEmpty()) {
                txt_nombres.requestFocus();
                throw new IllegalArgumentException("Ingrese los Nombres del Paciente");
            }
            if (txt_identificacion.getText().trim().isEmpty()) {
                txt_identificacion.requestFocus();
                throw new IllegalArgumentException("Ingrese el CI/Pasaporte del Paciente");
            }
            if (txt_fechaEmision.getDate() == null) {
                txt_fechaEmision.requestFocus();
                throw new IllegalArgumentException("Ingrese la Fecha de Emision");
            }
            if (txt_total.getText().trim().isEmpty()) {
                txt_total.requestFocus();
                throw new IllegalArgumentException("No se calculó el total");
            }

            String serviciosString = txt_servicio.getText().trim();
            if (serviciosString.isEmpty() && tab_detalles.getRowCount() > 0) {
                serviciosString = tab_detalles.getValueAt(0, 0).toString();
            } else if (serviciosString.isEmpty() && filaSel != -1) {
                serviciosString = tab_facturas.getValueAt(filaSel, 5).toString();
            }

            java.text.SimpleDateFormat formatoFecha = new java.text.SimpleDateFormat("dd/MM/yyyy");
            String fechaFormateada = formatoFecha.format(txt_fechaEmision.getDate());

            double totalReal = Double.parseDouble(txt_total.getText().trim());

            if (filaSel != -1) {
                String numFact = tab_facturas.getValueAt(filaSel, 0).toString();

                Clases.Cls_emitirFactura_CMI_V_1_3_1 objeto = new Clases.Cls_emitirFactura_CMI_V_1_3_1(
                        "",
                        fechaFormateada,
                        0.0,
                        "",
                        serviciosString,
                        "",
                        totalReal,
                        txt_identificacion.getText().trim(),
                        txt_apellidos.getText().trim(),
                        txt_nombres.getText().trim(),
                        "", "", "", "", "", ""
                );
                objeto.setFac_idInterno(numFact);
                objeto.setFac_total(totalReal);

                tab_facturas.setValueAt(txt_numFact.getText(), filaSel, 0);
                tab_facturas.setValueAt(txt_apellidos.getText(), filaSel, 1);
                tab_facturas.setValueAt(txt_nombres.getText(), filaSel, 2);
                tab_facturas.setValueAt(txt_identificacion.getText(), filaSel, 3);
                tab_facturas.setValueAt(fechaFormateada, filaSel, 4);
                tab_facturas.setValueAt(serviciosString, filaSel, 5);
                tab_facturas.setValueAt(txt_total.getText(), filaSel, 6);

                objeto.mtd_guardar();

            } else {
                Clases.Cls_emitirFactura_CMI_V_1_3_1 objeto = new Clases.Cls_emitirFactura_CMI_V_1_3_1(
                        "",
                        fechaFormateada,
                        0.0,
                        "",
                        serviciosString,
                        "",
                        totalReal,
                        txt_identificacion.getText().trim(),
                        txt_apellidos.getText().trim(),
                        txt_nombres.getText().trim(),
                        "", "", "", "", "", ""
                );

                objeto.setFac_total(totalReal);

                modeloFacturas = (DefaultTableModel) tab_facturas.getModel();
                String nuevaFila[] = {
                    objeto.getFac_idInterno(),
                    objeto.getPcs_apellidos(),
                    objeto.getPcs_nombres(),
                    objeto.getPcs_CiPasaporte(),
                    objeto.getFac_fechaEmision(),
                    serviciosString,
                    txt_total.getText()
                };
                modeloFacturas.addRow(nuevaFila);

                JOptionPane.showMessageDialog(null, "Factura emitida con éxito. Código: " + objeto.getFac_idInterno());

                modeloDetalles = (DefaultTableModel) tab_detalles.getModel();
                modeloDetalles.setRowCount(0);

                txt_nombres.setText("");
                txt_apellidos.setText("");
                txt_identificacion.setText("");
                txt_fechaEmision.setDate(null);

                objeto.mtd_guardar();
                mtd_limpiarDetalles();
            }
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error " + e.getMessage());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

