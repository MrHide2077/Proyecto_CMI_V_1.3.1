    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int filaSel = tab_consultaPacientes.getSelectedRow();
        try {
            if (filaSel != -1) {
                txt_idInterno.setText(tab_consultaPacientes.getValueAt(filaSel, 0).toString());
                txt_CiPasaporte.setText(tab_consultaPacientes.getValueAt(filaSel, 1).toString());
                txt_apellidos.setText(tab_consultaPacientes.getValueAt(filaSel, 2).toString());
                txt_nombres.setText(tab_consultaPacientes.getValueAt(filaSel, 3).toString());
                txt_genero.setSelectedItem(tab_consultaPacientes.getValueAt(filaSel, 4).toString().trim());
                txt_fechaNacimiento.setDate(formatoFecha.parse(tab_consultaPacientes.getValueAt(filaSel, 5).toString()));
                txt_telefono.setText(tab_consultaPacientes.getValueAt(filaSel, 6).toString());
                txt_correo.setText(tab_consultaPacientes.getValueAt(filaSel, 7).toString());
                txt_observa.setText(tab_consultaPacientes.getValueAt(filaSel, 8).toString());
                txt_tipoPaciente.setSelectedItem(tab_consultaPacientes.getValueAt(filaSel, 9).toString().trim());
            } else {
                JOptionPane.showMessageDialog(null, "Seleccione una fila");
            }
        } catch (java.text.ParseException e) {
            JOptionPane.showMessageDialog(null, "Error en el Formato de Fecha dd/mm/yyyy");
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(null, "Error Inesperado :" + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Clases.Cls_registroPacientesV_3_1 objeto = new Clases.Cls_registroPacientesV_3_1();
        int filaSelec = tab_consultaPacientes.getSelectedRow();
        if (filaSelec != -1) {
            int opcion = javax.swing.JOptionPane.showConfirmDialog(null, "¿Está seguro de eliminar este registro?");

            if (opcion == 0) {
                if (objeto.mtd_eliminar() == true) {
                    modelo = (DefaultTableModel) tab_consultaPacientes.getModel();
                    String idEliminado = tab_consultaPacientes.getValueAt(filaSelec, 0).toString();
                    modelo.removeRow(filaSelec);
                    javax.swing.JOptionPane.showMessageDialog(null, "Registro: " + idEliminado + " eliminado correctamente");
                    mtd_limpieza();
                }
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "Por favor Seleccione una Fila");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

