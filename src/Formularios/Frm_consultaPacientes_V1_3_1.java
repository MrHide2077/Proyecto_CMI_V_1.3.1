    private boolean mtd_duplicado() {
        boolean duplicado = false;
        for (int i = 0; i < tab_consultaPacientes.getRowCount(); i++) {
            if (txt_CiPasaporte.getText().equals(tab_consultaPacientes.getValueAt(i, 1).toString())) {
                duplicado = true;
                break;
            }
        }
        return duplicado;
    }

